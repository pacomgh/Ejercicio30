
package ejercicio30;


public class Temperatura {
    float faren, celsius;
    
    public void setFaren(float f){
        faren=f;
    }
    
    public float getFaren(){
        return faren;
    }
    
    public void setCelsius(float c){
        celsius=c;
    }
    
    public float getClsius(){
        return celsius;
    }    
}
